@dataclass(frozen=True)
class RunKey:
    device: str
    runtime: str
    model_id: str
    task: str


@dataclass
class Sample:
    path: Path
    run_index: int
    load_s: float | None
    ttft_ms: int
    prefill_tok_s: float
    decode_tok_s: float
    peak_mem_mb: float
    itl_p50_ms: float | None
    itl_p95_ms: float | None
    itl_p99_ms: float | None
    energy_j: float | None
    energy_j_per_tok: float | None
    avg_package_w: float | None
    energy_source: str | None
    quantization: str
    model_display: str
    params_b: float | None
    os_label: str
    build: str


def parse_sample(path: Path) -> tuple[RunKey, Sample] | None:
    try:
        data = json.loads(path.read_text(encoding="utf-8"))
    except Exception as exc:
        print(f"warn: skipping {path.name}: {exc}", file=sys.stderr)
        return None

    metrics = data.get("metrics") or {}
    model = data.get("model") or {}
    device = data.get("device") or {}

    stem = path.stem
    run_index_match = re.search(r"-run(\d+)$", stem)
    run_index = int(run_index_match.group(1)) if run_index_match else 1
    device_label = device_label_from_filename(stem)

    key = RunKey(
        device=device_label,
        runtime=data.get("runtime", "?"),
        model_id=model.get("id", "?"),
        task=data.get("task", "?"),
    )
    sample = Sample(
        path=path,
        run_index=run_index,
        load_s=metrics.get("loadTimeSeconds"),
        ttft_ms=int(metrics.get("firstTokenLatencyMS", 0)),
        prefill_tok_s=float(metrics.get("promptTokensPerSecond", 0.0)),
        decode_tok_s=float(metrics.get("decodeTokensPerSecond", 0.0)),
        peak_mem_mb=float(metrics.get("memoryPeakDuringDecodeMB", 0.0)),
        itl_p50_ms=metrics.get("interTokenLatencyP50MS"),
        itl_p95_ms=metrics.get("interTokenLatencyP95MS"),
        itl_p99_ms=metrics.get("interTokenLatencyP99MS"),
        energy_j=metrics.get("energyJoules"),
        energy_j_per_tok=metrics.get("energyJoulesPerToken"),
        avg_package_w=metrics.get("averagePackagePowerW"),
        energy_source=metrics.get("energySource"),
        quantization=model.get("quantization", "?"),
        model_display=model.get("displayName", model.get("id", "?")),
        params_b=model.get("parameterCountB"),
        os_label=device.get("systemName", "")
        + " "
        + (device.get("systemVersion", "").split(" ")[1] if device.get("systemVersion") else ""),
        build=device.get("buildConfiguration", "?"),
    )
    return key, sample


def load_samples() -> dict[RunKey, list[Sample]]:
    groups: dict[RunKey, list[Sample]] = {}
    for path in sorted(RAW_DIR.glob("*.jsonl")):
        out = parse_sample(path)
        if out is None:
            continue
        key, sample = out
        groups.setdefault(key, []).append(sample)
    for samples in groups.values():
        samples.sort(key=lambda s: s.run_index)
    return groups


