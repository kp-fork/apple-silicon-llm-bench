def render_per_run_row(key: RunKey, sample: Sample) -> str:
    return (
        f"| {runtime_display(key.runtime)} | {sample.model_display} | "
        f"{sample.quantization} | {sample.run_index} | "
        f"{fmt_load(sample.load_s)} | {fmt_int(sample.ttft_ms)} | "
        f"{fmt_one(sample.prefill_tok_s)} | {fmt_one(sample.decode_tok_s)} | "
        f"{fmt_one(sample.peak_mem_mb, places=0)} | "
        f"`{sample.path.name}` |"
    )


def render_full_dump(groups: dict[RunKey, list[Sample]]) -> str:
    lines = [
        "### Pivot 3 — full row dump (audit trail)",
        "",
        "Every raw measurement. Use Pivots 1 and 2 above for analysis; this table is the source of truth.",
        "",
        "| Runtime | Model | Quant | Run | Load (s) | TTFT (ms) | Prefill tok/s | Decode tok/s | Peak Mem (MB) | JSONL |",
        "|---|---|---|---:|---:|---:|---:|---:|---:|---|",
    ]
    ordered = sorted(
        groups.items(),
        key=lambda kv: (kv[0].device, kv[0].task, kv[0].runtime, kv[0].model_id),
    )
    for key, samples in ordered:
        for s in samples:
            lines.append(render_per_run_row(key, s))
    return "\n".join(lines)


def render_per_model_pivot(groups: dict[RunKey, list[Sample]]) -> str:
    """One sub-table per (device, task, logical_model) — rows are runtimes."""
    # group by (device, task, logical_model_name) → list of (runtime, samples)
    nested: dict[tuple[str, str, str], list[tuple[str, list[Sample]]]] = {}
    for key, samples in groups.items():
        bucket = (key.device, key.task, logical_model(key.model_id))
        nested.setdefault(bucket, []).append((key.runtime, samples))

    out: list[str] = [
        "## Pivot 1 — by model",
        "",
        "Each sub-table fixes the *logical* model (Gemma 4 E2B, Qwen 3.5 2B, …) and varies the runtime. Runtimes pull weights from different HF orgs, so the precise model id varies per row — the quant column makes the difference explicit.",
    ]
    for bucket, runtime_entries in sorted(nested.items()):
        device, task, logical_name = bucket
        device_lbl = device_display(device)
        out.append("")
        out.append(f"### {logical_name}  ({device_lbl}, {task})")
        out.append("")
        out.append(
            "| Runtime | Model ID | Quant | n | Load (s, median) | TTFT (ms, median) | Prefill tok/s (median) | Decode tok/s (median) | Peak Mem (MB, median) |"
        )
        out.append(
            "|---|---|---|---:|---:|---:|---:|---:|---:|"
        )
        # sort by runtime name for stability
        runtime_entries.sort(key=lambda x: x[0])
        for runtime, samples in runtime_entries:
            n = len(samples)
            s0 = samples[0]
            # show the model id (truncated if very long) so it's clear which
            # exact HF repo the row pulls from
            model_id = s0.path.name  # fallback won't be used because we have id
            # actually we want the model.id from JSON — stash it in Sample
            model_id_display = _model_id_for_display(s0)
            row = (
                f"| {runtime_display(runtime)} "
                f"| `{model_id_display}` "
                f"| {s0.quantization} "
                f"| {n} "
                f"| {fmt_load(median_or_only(s.load_s for s in samples))} "
                f"| {fmt_int(median_or_only(s.ttft_ms for s in samples))} "
                f"| {fmt_one(median_or_only(s.prefill_tok_s for s in samples))} "
                f"| {fmt_one(median_or_only(s.decode_tok_s for s in samples))} "
                f"| {fmt_one(median_or_only(s.peak_mem_mb for s in samples), places=0)} |"
            )
            out.append(row)
    return "\n".join(out)


def _model_id_for_display(sample: Sample) -> str:
    """Pull the original `model.id` back out for the per-model pivot rows."""
    try:
        data = json.loads(sample.path.read_text(encoding="utf-8"))
        return (data.get("model") or {}).get("id", "?")
    except Exception:
        return "?"


def render_per_runtime_pivot(groups: dict[RunKey, list[Sample]]) -> str:
    """One sub-table per (device, task, runtime) — rows are models."""
    nested: dict[tuple[str, str, str], list[tuple[str, list[Sample]]]] = {}
    for key, samples in groups.items():
        bucket = (key.device, key.task, key.runtime)
        nested.setdefault(bucket, []).append((key.model_id, samples))

    out: list[str] = [
        "## Pivot 2 — by runtime",
        "",
        "Each sub-table fixes the runtime and varies the model, so you can see how a single backend scales across model sizes within itself.",
    ]
    for bucket, model_entries in sorted(nested.items()):
        device, task, runtime = bucket
        device_lbl = device_display(device)
        out.append("")
        out.append(f"### `{runtime_display(runtime)}`  ({device_lbl}, {task})")
        out.append("")
        out.append(
            "| Model | Params (B) | Quant | n | TTFT (ms, median) | Decode tok/s (median) | Peak Mem (MB, median) |"
        )
        out.append("|---|---:|---|---:|---:|---:|---:|")
        # sort by parameter count if available else model id
        def sort_key(entry: tuple[str, list[Sample]]) -> tuple[float, str]:
            params = entry[1][0].params_b or 1e9
            return (params, entry[0])

        for model_id, samples in sorted(model_entries, key=sort_key):
            n = len(samples)
            s0 = samples[0]
            row = (
                f"| {s0.model_display} "
                f"| {s0.params_b if s0.params_b else '—'} "
                f"| {s0.quantization} "
                f"| {n} "
                f"| {fmt_int(median_or_only(s.ttft_ms for s in samples))} "
                f"| {fmt_one(median_or_only(s.decode_tok_s for s in samples))} "
                f"| {fmt_one(median_or_only(s.peak_mem_mb for s in samples), places=0)} |"
            )
            out.append(row)
    return "\n".join(out)


def render_at_a_glance(groups: dict[RunKey, list[Sample]]) -> str:
    """Pick the (device, task, logical_model) bucket with the most runtimes and emit its mini-table."""
    by_bucket: dict[tuple[str, str, str], list[tuple[str, list[Sample]]]] = {}
    for key, samples in groups.items():
        b = (key.device, key.task, logical_model(key.model_id))
        by_bucket.setdefault(b, []).append((key.runtime, samples))

    if not by_bucket:
        return "## At-a-glance\n\n_No data yet._"

    # Choose bucket with most runtimes; tie-break by total sample count.
    best = max(
        by_bucket.items(),
        key=lambda kv: (len(kv[1]), sum(len(s) for _, s in kv[1])),
    )
    bucket, runtime_entries = best
    device, task, logical_name = bucket
    runtime_entries.sort(key=lambda x: x[0])

    out: list[str] = [
        "## At-a-glance",
        "",
        f"The cross-runtime cell with the broadest coverage today — **{logical_name}** on **{device_display(device)}**, task **{task}**.",
        "",
        "| Runtime | Quant | n | TTFT (ms) | Decode tok/s | Prefill tok/s | Peak Mem (MB) |",
        "|---|---|---:|---:|---:|---:|---:|",
    ]
    for runtime, samples in runtime_entries:
        n = len(samples)
        row = (
            f"| {runtime_display(runtime)} "
            f"| {samples[0].quantization} "
            f"| {n} "
            f"| {fmt_int(median_or_only(s.ttft_ms for s in samples))} "
            f"| {fmt_one(median_or_only(s.decode_tok_s for s in samples))} "
            f"| {fmt_one(median_or_only(s.prefill_tok_s for s in samples))} "
            f"| {fmt_one(median_or_only(s.peak_mem_mb for s in samples), places=0)} |"
        )
        out.append(row)
    return "\n".join(out)


def render_coverage(groups: dict[RunKey, list[Sample]]) -> str:
    devices = sorted({k.device for k in groups})
    runtimes = sorted({k.runtime for k in groups})
    models = sorted({k.model_id for k in groups})
    tasks = sorted({k.task for k in groups})
    total_runs = sum(len(v) for v in groups.values())
    return (
        "## Coverage snapshot\n"
        "\n"
        f"- **Devices** ({len(devices)}): {', '.join(device_display(d) for d in devices)}\n"
        f"- **Runtimes** ({len(runtimes)}): {', '.join(runtime_display(r) for r in runtimes)}\n"
        f"- **Models** ({len(models)}): {', '.join(models)}\n"
        f"- **Tasks** ({len(tasks)}): {', '.join(tasks)}\n"
        f"- **Total runs**: {total_runs}\n"
    )


