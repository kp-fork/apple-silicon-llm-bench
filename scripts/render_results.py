#  Aggregation helpers
# ------------------------------------------------------------------ #


def median_or_only(values: Iterable[float]) -> float:
    xs = [v for v in values if v is not None]
    if not xs:
        return 0.0
    return statistics.median(xs)


def fmt_int(x: float) -> str:
    return f"{int(round(x))}"


def fmt_one(x: float | None, places: int = 1) -> str:
    if x is None:
        return "—"
    if x == 0:
        return "—"
    return f"{x:.{places}f}"


def fmt_load(x: float | None) -> str:
    if x is None:
        return "—"
    return f"{x:.1f}"


def device_display(label: str) -> str:
    return DEVICE_DISPLAY.get(label, label)


