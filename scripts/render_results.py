#!/usr/bin/env python3
"""
Yardstick — render RESULTS.md tables from results/raw/*.jsonl.

The harness writes one `BenchmarkResult` JSONL per run. Each filename
encodes the device label as its first dash-separated segment, e.g.

    results/raw/m4max-mlx-gemma-4-e2b-short-chat-run2.jsonl
                ^^^^^

The runtime, model, task, and metrics come from the JSON itself, so the
table layout is robust against typos in the rest of the filename. The
filename's `-run<N>` suffix (optional) is used purely to keep multiple
runs of the same `(device, runtime, model, task)` distinct on disk.

Usage:

    python scripts/render_results.py            # rewrite RESULTS.md in place
    python scripts/render_results.py --check    # exit 1 if RESULTS.md would change

The script edits only the block between

    <!-- BEGIN: generated by scripts/render_results.py -->
    <!-- END: generated by scripts/render_results.py -->

so the hand-written intro, methodology, and failed-runs sections survive.

Adding a row is therefore: drop a JSONL into `results/raw/`, run the
script, commit. Nothing else.
"""

